public class contohtipedatabyte {
    public static void main(String[] args) {
        // Deklarasi variabel tipe data byte
        byte x = 10;
        byte y = 20;

        // Menjumlahkan dua byte
        byte sum = (byte) (x + y);

        // Menampilkan hasil
        System.out.println("Nilai x: " + x);
        System.out.println("Nilai y: " + y);
        System.out.println("Hasil penjumlahan x + y: " + sum);
    }
}
