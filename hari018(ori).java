public class ContohBoolean {
    public static void main(String[] args) {

        boolean isRaining = false; 
        
        if (isRaining) {
            System.out.println("Bawa payung!");
        } else {
            System.out.println("Cuaca cerah, tidak perlu payung.");
        }

        isRaining = true; 
        
        if (isRaining) {
            System.out.println("Bawa payung!");
        } else {
            System.out.println("Cuaca cerah, tidak perlu payung.");
        }
    }
}
