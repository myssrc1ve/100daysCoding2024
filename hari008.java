public class Update {
    public static void main(String[] args) {
        // Mendefinisikan nama barang awal
        String namaBarang = "Lampu";

        // Menampilkan nama barang sebelum update
        System.out.println("Sebelum update: " + namaBarang);

        // Mengupdate nama barang
        namaBarang = "Lampu LED";

        // Menampilkan nama barang setelah update
        System.out.println("Setelah update: " + namaBarang);
    }
}
