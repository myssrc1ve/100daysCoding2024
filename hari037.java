public class OperatorPenugasan {
    public static void main(String[] args) {

        int a = 10;
        System.out.println("Nilai a: " + a);

        int b = 5;
        b += 3; 
        System.out.println("Nilai b setelah penambahan: " + b);

        int c = 20;
        c -= 4; 
        System.out.println("Nilai c setelah pengurangan: " + c);
    }
}
