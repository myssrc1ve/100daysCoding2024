public class LoopWhileExample {
    public static void main(String[] args) {
        int i = 1; // Inisialisasi variabel

        // Looping while
        while (i <= 10) { // Kondisi looping
            System.out.println("Nilai i: " + i); // Menampilkan nilai i
            i++; // Increment nilai i
        }
    }
}
