public class ContohChar {
    public static void main(String[] args) {
        
        char initial = 'A'; 
        char grade = 'B';

        System.out.println("Inisial: " + initial);
        System.out.println("Nilai: " + grade);

        int asciiInitial = initial;
        int asciiGrade = grade;

        System.out.println("Nilai ASCII dari " + initial + " adalah: " + asciiInitial);
        System.out.println("Nilai ASCII dari " + grade + " adalah: " + asciiGrade);
    }
}
