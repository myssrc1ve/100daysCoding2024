public class InfoWisata {
    public static void main(String[] args) {
        // Deklarasi variabel dengan nilai
        String objekWisata = "Pantai Dato";  // Nama objek wisata
        int jumlahPengunjung = 500;          // Jumlah pengunjung per hari
        float hargaTiket = 25000.50f;        // Harga tiket masuk, tipe float untuk desimal
        boolean bukaSetiapHari = true;       // Status apakah objek wisata buka setiap hari

        // Menampilkan nilai variabel
        System.out.println("Objek Wisata: " + objekWisata);
        System.out.println("Jumlah Pengunjung per Hari: " + jumlahPengunjung);
        System.out.println("Harga Tiket: Rp" + hargaTiket);
        System.out.println("Buka Setiap Hari: " + bukaSetiapHari);
    }
}
