public class Perbandingan {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;

        if (a == b) {
            System.out.println("a sama dengan b");
        } else {
            System.out.println("a tidak sama dengan b");
        }

        if (a != b) {
            System.out.println("a tidak sama dengan b");
        } else {
            System.out.println("a sama dengan b");
        }
    }
}
